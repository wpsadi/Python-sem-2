# all of the following are equivalent
my_string = 'Hello'
print(my_string)

my_string = "Hello"
print(my_string)

my_string = '''Hello'''
print(my_string)


# triple quotes string can extend multiple lines
my_string = """Hello, welcome to
 the world of Python"""
print(my_string)


c=" mlritm"
print(my_string+c)


# substring function
print(my_string[5:11])

print(my_string[::-1])
print(my_string[-1::-1])